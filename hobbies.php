                    <br>As the armies that you can buy and paint are described in depth within their own rulebook and have their own history, legends and beliefs, making the Grim and Dark universe of Warhammer 40k feel more coherent and feeling like a sort of "rabbit hole" of stories.
                    <br>The second thing I love about Warhammer 40k is the actual playing and strategies of the game, with every single army having a varying level of depth and different tactics you can use in order to win a Battle.
                    <br>The third thing I really like about 40k is that the actual process of building up an army of miniatures, building them, adding more flair, and painting them, feels very satisfying to me, it allows me to cool my head and take a few steps back and think a little about some other stuff while my brain "switches off".</p>
